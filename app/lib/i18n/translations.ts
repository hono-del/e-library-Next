export function getNextWorkflowPhaseLocalized(currentPhaseId: string, locale: Locale) {
  const phases = getWorkflowPhases(locale)
  const index = phases.findIndex((p) => p.id === currentPhaseId)
  if (index >= 0 && index < phases.length - 1) return phases[index + 1]
  return null
}

export function getPreviousWorkflowPhaseLocalized(currentPhaseId: string, locale: Locale) {
  const phases = getWorkflowPhases(locale)
  const index = phases.findIndex((p) => p.id === currentPhaseId)
  if (index > 2) return phases[index - 1]
  return null
}
